#!/usr/bin/env node

let score = 55

// Determine the grade based on the marks using if-else statements
if (score >= 90) {
    console.log("Grade: A")
} 
else if (score >= 80) {
    console.log("Grade: B")
}
else if (score >= 70) {
    console.log("Grade: C")
}
else if (score >= 60) {
    console.log("Grade: D")
} 
else if (score <60) {
    console.log("Grade: F")
}